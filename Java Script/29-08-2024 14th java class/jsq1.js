//Arrow function
let mod =(a,b)=> {
    console.log(a%b);
}
mod(20,10);

// * let mod = (a, b) => { ... }: This is an arrow function that takes two parameters, a and b.
// * console.log(a % b);: This line calculates the remainder when a is divided by b and prints the result to the console.
// * mod(20, 10);: This line calls the mod function with arguments 20 and 10.